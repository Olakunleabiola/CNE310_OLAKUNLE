def print_person_info(name, age, weight, height):
    print("Name:", name)
    print("Age:", age)
    print("Weight:", weight)
    print("Height:", height//12, "ft", height%12, "inches")
    print()

print_person_info("Joe", 22, 123, 69)
print_person_info("Josh", 32, 156, 74)
print_person_info("Mabel", 21, 106, 62)